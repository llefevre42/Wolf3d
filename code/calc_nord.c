#include "wolf3d.h"

int	calc_nord(t_tri *lst)
{
	if (1 == lst->map_x - lst->sd_x)
		lst->color == 0XF0F0F0;
}

int	calc_west(t_tri *lst)
{
	if (1 == lst->map_x - lst->sd_x)
		lst->color == 0X0F0F0F;
}


